	<h2>Activités</h2>
	<p>Les activités de la région comprennent le tennis, la voile, le kitesurf, 
	les promenades en bateau dans la baie, la talassothérapie, la visite d’ un grand aquarium.</p> 
	<p>Il est possible de faire des excursions à Chausey, Jersey, Guernesey, la descente de la Rance en bateau-croisière jusqu’ à dinan. 
	Un bateau bus assure la liaison avec dinard plusieurs fois par jours.</p>
	<p>
	Une navette assure également la liaison avec l’île de Cézembre où la plage est plein sud.</p>
