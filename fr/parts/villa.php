			Cette maison de villégiature construite en 1923 par Yves Hémar dont elle serait une oeuvre de jeunesse  a beaucoup de charme. Elle est situé à Saint-Malo au bord de la grande plage et se trouve à 20 minutes à pied d’ intra-muros, à 10 minutes à pieds de la gare (je peux venir vous chercher à la gare) et à 5 minutes à pieds des Thermes Marins. Sa situation sur la digue vous garantit le calme.
			</p>
